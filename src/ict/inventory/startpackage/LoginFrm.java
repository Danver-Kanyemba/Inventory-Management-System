    private void createUserBtnbyAdminActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createUserBtnbyAdminActionPerformed
        String username = usernameTxt.getText().toString();
        String password = new String(passwordTxtpass.getPassword());
        boolean isInputCorrect = true;
        
        if(username.isEmpty()){
            JOptionPane.showMessageDialog(null,"Username is required");
  
            isInputCorrect = false;
        }
        if(password.isEmpty()){
            JOptionPane.showMessageDialog(null,"password is required");
            isInputCorrect = false;
        }


        if(isInputCorrect){
            try {
                ConnectToDB DBcon = new ConnectToDB();
                try {
                    String queryCheckUser = "SELECT * FROM users "
                            + " WHERE username =? AND password=? ";
                    PreparedStatement pst = DBcon.conn.prepareStatement(queryCheckUser);
                    pst.setString(1, username);
                    pst.setString(2, password);
                    ResultSet rsv = pst.executeQuery();
                    System.out.println(DBcon.conn);                    
                    System.out.println(password);
                    if (rsv.next()){
                        String queryCheckRole = "SELECT * FROM users WHERE username = '"+username+"'";
                        Statement st = DBcon.conn.createStatement();
                        ResultSet rs = st.executeQuery(queryCheckRole);
                        while(rs.next()){
                            roleForUser = rs.getString("role");
                            idForUser = rs.getString("user_id");
                            System.out.println(roleForUser);
                        }
                        JOptionPane.showMessageDialog(null,"Log in successful");
                        ict.inventory.ordinaryuser.MainMenu MainMenu = new ict.inventory.ordinaryuser.MainMenu(idForUser,username, roleForUser);
                        
                        MainMenu.setVisible(true);
                        this.setVisible(false);
                        this.dispose();
                        
                    }
                    else{
                        JOptionPane.showMessageDialog(null,"Username and password does not match");
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(LoginFrm.class.getName()).log(Level.SEVERE, null, ex);
                }
                finally{
                    DBcon.conn.close();
                    
                }
            } catch (ClassNotFoundException | SQLException ex) {
                Logger.getLogger(LoginFrm.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null,"Error: MySQL Connection not"
                        + " established or Database username or password not correct");

                }
        }
    }//GEN-LAST:event_createUserBtnbyAdminActionPerformed

